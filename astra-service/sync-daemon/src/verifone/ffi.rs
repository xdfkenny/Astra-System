//! Thin FFI bindings for the Verifone C SDK.
//!
//! This is the only module in the crate that uses `unsafe`.  It exists because
//! the vendor SDK is distributed as a C library and Rust has no mechanism to
//! call C functions without `extern "C"` blocks and raw pointers.  All unsafe
//! operations are confined here and wrapped with enough validation that the
//! public [`crate::verifone`] API is safe to call.
//!
//! Invariants maintained by this module:
//! * Function pointers are only dereferenced after the SDK reports success.
//! * Output buffers are fully zero-initialized before being passed to C.
//! * Returned C strings are converted to Rust strings by the safe API layer.
//! * The opaque `VxTerminal` pointer is owned by [`crate::verifone::TerminalHandle`].

#![allow(unsafe_code)]

use crate::verifone::error::VerifoneError;

// The C types below mirror `packages/verifone-ffi/include/verifone.h`.
// In a build that links the real SDK these would be generated by bindgen.

#[repr(C)]
pub struct VxTerminal {
    _private: [u8; 0],
}

#[repr(C)]
#[derive(Debug, Copy, Clone)]
pub struct VxCardInfo {
    pub present: u8,
    pub brand: [std::ffi::c_char; 32],
    pub last_four: [std::ffi::c_char; 5],
}

#[repr(C)]
#[derive(Debug, Copy, Clone)]
pub struct VxPaymentResult {
    pub result: i32,
    pub auth_code: [std::ffi::c_char; 16],
    pub opaque_token: [std::ffi::c_char; 256],
    pub terminal_id: [std::ffi::c_char; 64],
    pub authorized_amount_cents: u64,
}

#[repr(C)]
#[derive(Debug, Copy, Clone)]
pub struct VxRefundResult {
    pub result: i32,
    pub reference: [std::ffi::c_char; 128],
    pub refunded_amount_cents: u64,
}

extern "C" {
    pub fn vx_init_terminal(out_terminal: *mut *mut VxTerminal) -> i32;
    pub fn vx_start_transaction(
        terminal: *mut VxTerminal,
        amount_cents: u64,
        currency: *const std::ffi::c_char,
    ) -> i32;
    pub fn vx_wait_for_card(
        terminal: *mut VxTerminal,
        timeout_ms: u32,
        out_card: *mut VxCardInfo,
    ) -> i32;
    pub fn vx_process_payment(terminal: *mut VxTerminal, out_result: *mut VxPaymentResult) -> i32;
    pub fn vx_refund(
        terminal: *mut VxTerminal,
        transaction_id: *const std::ffi::c_char,
        amount_cents: u64,
        currency: *const std::ffi::c_char,
        out_result: *mut VxRefundResult,
    ) -> i32;
    pub fn vx_close_terminal(terminal: *mut VxTerminal) -> i32;
}

/// Safe wrapper around `vx_init_terminal`.
pub fn init_terminal() -> Result<*mut VxTerminal, VerifoneError> {
    let mut ptr: *mut VxTerminal = std::ptr::null_mut();
    let rc = unsafe { vx_init_terminal(&mut ptr) };
    VerifoneError::from_result(rc)?;
    if ptr.is_null() {
        return Err(VerifoneError::UnexpectedNullHandle);
    }
    Ok(ptr)
}

/// Safe wrapper around `vx_start_transaction`.
pub fn start_transaction(
    ptr: *mut VxTerminal,
    amount_cents: u64,
    currency: &std::ffi::CStr,
) -> Result<(), VerifoneError> {
    let rc = unsafe { vx_start_transaction(ptr, amount_cents, currency.as_ptr()) };
    VerifoneError::from_result(rc)
}

/// Safe wrapper around `vx_wait_for_card`.
pub fn wait_for_card(ptr: *mut VxTerminal, timeout_ms: u32) -> Result<VxCardInfo, VerifoneError> {
    let mut info: VxCardInfo = unsafe { std::mem::zeroed() };
    let rc = unsafe { vx_wait_for_card(ptr, timeout_ms, &mut info) };
    VerifoneError::from_result(rc)?;
    Ok(info)
}

/// Safe wrapper around `vx_process_payment`.
pub fn process_payment(ptr: *mut VxTerminal) -> Result<VxPaymentResult, VerifoneError> {
    let mut result: VxPaymentResult = unsafe { std::mem::zeroed() };
    let rc = unsafe { vx_process_payment(ptr, &mut result) };
    VerifoneError::from_result(rc)?;
    Ok(result)
}

/// Safe wrapper around `vx_refund`.
pub fn refund(
    ptr: *mut VxTerminal,
    transaction_id: &std::ffi::CStr,
    amount_cents: u64,
    currency: &std::ffi::CStr,
) -> Result<VxRefundResult, VerifoneError> {
    let mut result: VxRefundResult = unsafe { std::mem::zeroed() };
    let rc = unsafe {
        vx_refund(
            ptr,
            transaction_id.as_ptr(),
            amount_cents,
            currency.as_ptr(),
            &mut result,
        )
    };
    VerifoneError::from_result(rc)?;
    Ok(result)
}

/// Safe wrapper around `vx_close_terminal`.
pub fn close_terminal(ptr: *mut VxTerminal) -> Result<(), VerifoneError> {
    let rc = unsafe { vx_close_terminal(ptr) };
    VerifoneError::from_result(rc)
}

