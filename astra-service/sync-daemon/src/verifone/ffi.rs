#[cfg(test)]
mod tests {
    use super::*;
    use super::stubs::set_next_result;

    #[test]
    fn ffi_init_terminal_success() {
        unsafe { set_next_result(0) };
        let ptr = init_terminal().expect("init should succeed");
        assert!(!ptr.is_null());
        let _ = close_terminal(ptr);
    }

    #[test]
    fn ffi_init_terminal_failure() {
        unsafe { set_next_result(5) };
        assert!(init_terminal().is_err());
        unsafe { set_next_result(0) };
    }
}
